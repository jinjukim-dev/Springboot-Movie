package org.study.admin;

public class MovieVO {
	private int movie_code;
	private String movie_sub;
	private String movie_direc;
	private String movie_gen;
	private String movie_open;
	private String movie_age;
	private String movie_time;
	private String movie_poster;
	public int getMovie_code() {
		return movie_code;
	}
	public void setMovie_code(int movie_code) {
		this.movie_code = movie_code;
	}
	public String getMovie_sub() {
		return movie_sub;
	}
	public void setMovie_sub(String movie_sub) {
		this.movie_sub = movie_sub;
	}
	public String getMovie_direc() {
		return movie_direc;
	}
	public void setMovie_direc(String movie_direc) {
		this.movie_direc = movie_direc;
	}
	public String getMovie_gen() {
		return movie_gen;
	}
	public void setMovie_gen(String movie_gen) {
		this.movie_gen = movie_gen;
	}
	public String getMovie_open() {
		return movie_open;
	}
	public void setMovie_open(String movie_open) {
		this.movie_open = movie_open;
	}
	public String getMovie_age() {
		return movie_age;
	}
	public void setMovie_age(String movie_age) {
		this.movie_age = movie_age;
	}
	public String getMovie_time() {
		return movie_time;
	}
	public void setMovie_time(String movie_time) {
		this.movie_time = movie_time;
	}
	public String getMovie_poster() {
		return movie_poster;
	}
	public void setMovie_poster(String movie_poster) {
		this.movie_poster = movie_poster;
	}
	
}
